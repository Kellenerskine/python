x1 = float(input("Type in the first x coordinate:"))
y1 = float(input("Type in the first y coordinate:"))
x2 = float(input("Type in the second x coordinate:"))
y2 = float(input("Type in the second y coordinate:"))

x_midpoint = (x1+x2)/2
y_midpoint = (y1+y2)/2

print("The midpoint is:", "(" + str(x_midpoint) + ", " + str(y_midpoint) + ")")